            <p>Also known as blepharoplasty, eyelid surgery improves the appearance of the upper eyelids, lower eyelids, or both. It gives a rejuvenated appearance to the surrounding area of your eyes, making you look more rested and alert.</p>
            <h4>Treatable conditions:</h4>
            <ul>
                <li>Upper eyelid surgery can remove excess fatty deposits that appear as puffiness in the upper eyelids.</li>
                <li>Loose or sagging skin that creates folds or disturbs the natural contour of the upper eyelid, sometimes impairing vision, can be treated by eyelid lift surgery.</li>
                <li>Lower eyelid blepharoplasty can remove excess skin and fine wrinkles of the lower eyelid.</li>
                <li>Bags under the eyes can be corrected by blepharoplasty.</li>
                <li>Lower eyelid surgery can correct droopiness of the lower eyelids, showing white below the iris (colored portion of the eye).</li>
            </ul>
            <h4>Is it right for me?</h4>
            <p>A blepharoplasty procedure is usually performed on adult men and women who have healthy facial tissue and muscles and have realistic goals for improvement of the upper and/or lower eyelids and surrounding area.</p>
            <p>You should undergo blepharoplasty surgery for yourself, not to fulfill someone else's desires or to try to fit any sort of ideal image.</p>
            <h4>Good candidates for cosmetic eyelid surgery are:</h4>
            <ul>
                <li>Healthy individuals who do not have a life-threatening illness or medical conditions that can impair healing</li>
                <li>Non-smokers</li>
                <li>Individuals with a positive outlook and specific goals in mind for blepharoplasty</li>
                <li>Individuals without serious eye conditions</li>
            </ul>
            <h4>You must tell your doctor if you have any of these medical conditions:</h4>
            <ul>
                <li>Eye disease such as glaucoma, dry eye or a detached retina</li>
                <li>Thyroid disorders such as Gravesâ€™ disease and under or overactive thyroid</li>
                <li>Cardiovascular disease, high blood pressure or other circulatory disorders or diabetes</li>
            </ul>
            <h4>Eyelid surgery at a glance</h4>
