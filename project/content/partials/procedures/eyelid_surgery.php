            <h2>What to expect during your eyelid surgery consultation <a class="toc_link" href="#procedure_table_of_contents">back to table of contents</a></h2>

            <br>
            <p>The success and safety of your eyelid surgery procedure depends very much on your complete candidness during your cosmetic eyelid surgery consultation. You'll be asked a number of questions about your health, desires and lifestyle.</p>

            <ul>
                <li>Why you want the surgery, your expectations and desired outcome</li>
                <li>Medical conditions, drug allergies and previous medical treatments</li>
                <li>Use of current medications, vitamins, herbal supplements, alcohol, tobacco and drugs</li>
                <li>Previous surgeries</li>
            </ul>
            <h4>Your surgeon may also:</h4>
            <ul>
                <li>Evaluate your general health status and any pre-existing health conditions or risk factors</li>
                <li>Take photographs for your medical record</li>
                <li>Discuss your options and recommend a course of treatment</li>
                <li>Discuss likely outcomes of eyelid surgery and any risks or potential complications</li>
                <li>Discuss the type of anesthesia that will be used</li>
            </ul>
