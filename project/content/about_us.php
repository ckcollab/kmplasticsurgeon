			<h3>Biography</h3>
			<p>Board certified Plastic surgeon, Dr. Kaiulani Morimoto grew up in Hawaii and graduated from Kalaheo High School. She then went on to obtain two degrees, an Associate of Arts in Liberal Arts and a Bachelor of Arts in Biology, from the University of Hawaii. Prior to attending medical school, she obtained certification as an EMT, and then worked actively in this field throughout her medical school years at John A. Burns School of Medicine. Once graduated, she completed three years of General Surgery at Virginia Mason Medical Center in Seattle, WA. The following three years consisted of back to back fellowships in Microsurgery (Christus St. Joseph Hospital, Houston, TX), Clinical Burn Research (Shriner’s Hospital, Galveston, TX) and Orthopedic Hand Surgery (Baylor College of Medicine, Houston, TX). She then completed a rigorous two-year fellowship in Plastic Surgery under the guidance of Dr. Ian Jackson at Providence Hospital in Detroit, MI. </p>

			<p>She has been a Plastic Surgeon in Spokane, WA since 2004 and has consistently been named Top Doctor since 2008. Despite a busy lifestyle both at work and home, she continues to stay on the cutting edge of Plastic Surgery by attending many local, state and national meetings. In addition, she is also passionate about health and wellness and currently is doing a fellowship in Anti-aging and Regenerative Medicine. She firmly believes that “by optimizing healthy lifestyles through diet and exercise, both before and after surgery, this will ensure success” of her procedures in the long run. Dr. Morimoto performs many cosmetic, reconstructive and hand surgeries, as well as, minimally invasive procedures such as Botox and fillers.</p>
